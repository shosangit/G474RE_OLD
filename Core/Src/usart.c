/* 串口接收空闲中断 */
void USER_UART_IRQHandler(UART_HandleTypeDef *huart)
{   // 判断是否是串口1
    if(huart->Instance == USART1)
    {   // 判断是否是空闲中断
        if(RESET != __HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE))
        {   // 清除空闲中断标志（否则会一直不断进入中断）
            __HAL_UART_CLEAR_IDLEFLAG(&huart1);
//            printf("\r\nUART1 Idle IQR Detected\r\n");
            HAL_Delay(15);
            // 调用中断处理函数
            USAR_UART_IDLECallback(huart);
        }

    }
}

void USAR_UART_IDLECallback(UART_HandleTypeDef *huart)
{
    // 停止本次DMA传输
    HAL_UART_DMAStop(&huart1);
    // 计算接收到的数据长度
    uint8_t data_length  = 1024 - __HAL_DMA_GET_COUNTER(&hdma_usart1_rx);
    // 测试函数：将接收到的数据打印出去
    printf("Receive Data(length = %d): \r\n",data_length);
    usart1_rx_flag=1;
    strcat((char*)usart1_rx_buffer,(char*)usart1_dma_buffer);
    memset(usart1_dma_buffer, 0, sizeof(*usart1_dma_buffer));
    printf("rx->%s\r\n",usart1_rx_buffer);
    // 重启开始DMA传输 每次255字节数据
    HAL_UART_Receive_DMA(&huart1, (uint8_t*)usart1_dma_buffer, 1024);
}

